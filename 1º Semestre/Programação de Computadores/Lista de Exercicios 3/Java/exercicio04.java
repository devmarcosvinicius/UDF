/*
 * 4. Elabore um algoritmo que imprima 100 vezes o texto "1- Hello World!" com o número.
 */

public class exercicio04 {

	public static void main(String[] args) {
		for(int i = 1; i <= 100; i++) {
			System.out.printf("%d- Hello World!\n", i);
		}
	}
	
}
